           <center><img src="images/plaga dos.jpg." alt=""></center>  
            <br>
            <P>  
            En plantaciones que colinden con montaña, bosque secundario o charrales, se debe dejar una distancia al menos de quince metros. En los alrededores de la plantación, se deben colocar bolsas de color rojo con “adherente”, a modo de trampas, para control y monitoreo de los adultos (mariposas). Estas trampas se deben colocar antes de la floración, alrededor de la sexta semana, después del forzamiento. Se deben identificar y eliminar las plantas hospederas, como las heliconias o plantillasen los alrededores de las plantaciones. Como control biológico, puede aplicarse la bacteria Bacillus thurigiensis (cepa kurstaki). Este método de control, debe ser aplicado en el momento correcto, ya que la bacteria actúa únicamente sobre la larva por contacto e ingestión. Si existe una alta incidencia, pueden aplicarse productos químicos a base de diazinón, carbaril y lambda-cihalotrina, entre otros registrados para el cultivo y la plaga, según la dosis indicada en la etiqueta.
