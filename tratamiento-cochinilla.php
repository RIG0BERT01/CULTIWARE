           <center><img src="images/plagauno.jpg" alt=""></center> 
            <br>
            <P>  
            La presencia de cochinillas está estrechamente relacionada con la población de hormigas, las cuales protegen a la Cochinilla de los ataques de los parásitos, depredadores y mantienen limpia la colonia de la presencia de hongos que puedan afectar a la cochinilla como a la planta. Se han observado casos donde hacen construcciones de barro alrededor de las plantas para protegerlas, eliminan las cochinillas enfermas o muertas y las transportan de un lugar a otra en la misma planta o dentro de la plantación, el beneficio que obtienen las hormigas son exudados ricos en azúcares que producen las cochinillas. Su población aumenta en la época de lluvias y disminuye en la época seca. El control de esta plaga resulta muy difícil, debido a que puede afectar en cualquier etapa de desarrollo del cultivo. Se debe desarrollar un plan de monitoreo constante, que abarque todas las etapas del cultivo y debe comenzar en los semilleros, donde se debe evitar al máximo transportar semilla infectada a las nuevas áreas de siembra.
            </P>
