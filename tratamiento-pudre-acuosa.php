            <center><h4>Pudrición acuosa - Thielaviopsis paradoxa</h4></center>
           <center><img src="images/enfermedad3.jpg" alt=""></center> 
            <br>
            <P>  
            Afecta el material de siembra, tallo, hojas y frutos. Su presencia en los hijos de siembra se distingue por un color gris oscuro en la base y un moteado blanco. En los frutos, ocurre una pudrición suave y acuosa. Es necesario realizar un adecuado secado del corte de los hijos antes de utilizarlos. Se deben eliminar en campo las frutas que presenten esta enfermedad, para no llevarlas a la planta empacadora. Se deben desinfectar los hijos de piña antes de la siembra.

