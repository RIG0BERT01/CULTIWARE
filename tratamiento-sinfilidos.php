            <center><h4>Sinfílidos - Symphylella spp</h4></center>
           <center><img src="images/plagacinco.jpg" alt=""></center> 
            <br>
            <P>  
            Los sinfilidos son débiles y frágiles, incapaces de hacer sus propias galerías por lo que utilizan las grietas del suelo, orificios de las raíces muertas, son muy activos y nerviosos y cuando se exponen a la luz se dispersan rápidamente. Prefiere suelos sueltos y bien drenados.
            El daño ocasionado se observa principalmente en las raíces secundarias y terciarios, en ellas se presentan cortes transversales de los ápices ocasionado por las mordeduras de los sinfílidos, de manera que cuando la raíz cicatriza produce otras raíces que crecen en forma desordenada produciendo el síntoma de daño conocido como escoba de bruja, además se observa una reducción de los pelos absorbentes. Buen manejo de residuos de la cosecha anterior y vigilancia en plantaciones vecinas. Mantener buenos drenajes. Existen productos repelentes que se pueden aplicar como el Bromorex, Dazitol, entre otros productos. Productos a base de chile, ajo, cebolla y/o mostaza.  
            </P>
