            <center><h4>Fusariosis - Fusarium oxysporum</h4></center>
           <center><img src="images/enfermedad4.png" alt=""></center> 
            <br>
            <P>  
            Identificación y características: Este hongo presenta una gama muy amplia de hospederos. Puede sobrevivir en hijos, frutas, corona, residuos vegetales y en el suelo. Su diseminación puede ocurrir por las partículas de suelo, viento, lluvia, insectos, semillas y material vegetal. Se debe preparar adecuadamente el terreno, con el fin de incorporar en su totalidad los rastrojos de plantación y reducir la humedad. La aplicación del hongo Trichoderma, en la etapa de preparación de terreno o en los primeros meses de desarrollo de la plantación, reduce la incidencia de Fusarium en el cultivo. Para el control químico, se pueden aplicar las moléculas carbendazín, procloraz y propiconazol.

