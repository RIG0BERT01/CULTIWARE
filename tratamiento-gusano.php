           <center><img src="images/plaga tres.png" alt=""></center> 
            <br>
            <P>  
            Atacan de manera ocasional y producen un daño directo a la fruta, el cual consiste en que comen partes de la cáscara y de la pulpa, provocando con ello que la fruta no reun a la calidad necesaria para la exportación. Son plagas que aparecen de manera ocasional, las cuales deben mantenerse bajo control, para evitar pérdidas de fruta mayores.
            Esta plaga generalmente aparece cuando la fruta le faltan pocas semanas para la cosecha (8 semanas antes de la cosecha). Su control debe hacerse cuando el monitoreo indique la presencia de la misma. Para el control de esta plaga se utilizan los mismos productos que para el control de la mariposa tecla, lo que varía es el intervalo de las aplicaciones, ya que en este caso se puede aplicar cada 22 días. Esto depende del grado de infestación de la plaga.
