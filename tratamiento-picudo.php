            <center><h4>Picudo - Metamasius dimidiatipennis</h4></center>
           <center><img src="images/plagacuatro.jpg" alt=""></center> 
            <br>
            <P>  
            Puede afectar el cultivo en cualquier etapa, cuando no se hace una buena preparación de terreno cuanto el manejo de los rastrojos de cosecha de plantaciones vecinas no es adecuado.
            Causan problemas cuando se encuentran en grandes poblaciones o bien cuando están fuera de control. Las hembras del picudo se alimentan de los tejidos internos de la planta de piña, haciendo agujeros para poner sus huevos. Las larvas emergidas, cavan galerías para alimentarse del tejido fresco, al completar su desarrollo construyen una envoltura con las fibras para iniciar la etapa de pupa. A los pocos días (14 -16 días) emergen los adultos, los cuales alcanzan su madurez sexual rápidamente.
            Las medidas de control de este insecto son las siguientes: Buen manejo de residuos de la cosecha anterior y vigilancia en plantaciones vecinas. Eliminación de hospederos alternos: como plantaciones de palma, palmito, plátano y/o banano. Cuando hay presencia se pueden utilizar hormonas atrayentes en combinación con el uso de trampas. Aplicación de hongos entomopatógenos como Beauveria y Metarrizium. 
            </P>
