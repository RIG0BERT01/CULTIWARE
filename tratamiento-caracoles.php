           <center><img src="images/plagasiete.jpg" alt=""></center> 
            <br>
            <P>  
            Su ciclo tiene una duración de cuarenta y cinco a sesenta días. Pueden vivir de seis meses a un año. Son hermafroditas y auto fecundantes. Ponen alrededor de treinta y cinco huevos por mes. Se alimentan de las raíces y hojas, por lo cual pueden ocasionar raspado y perforaciones. También, provocan un lento crecimiento de la planta, falta de uniformidad en la plantación y hojas angostas. Transmiten virus y enfermedades a los humanos.  Por ser de hábitos nocturnos, su control debe realizarse durante las noches.
            Se deben colocar trampas, como “sacos de gangoche” húmedos o tablas de madera húmedas; una vez atraídos, se deben eliminar físicamente o capturándolos y exponiéndolos al sol. Se deben lavar las cajas de transporte de fruta, porque se adhieren a estas y se dispersan por toda la plantación. No se recomienda aplicar carbonato de calcio para su control, ya que acorta el ciclo reproductivo y favorece su reproducción. Se deben controlar las malezas en los alrededores, así como los troncos en descomposición, los cuales pueden servir como hospederos.
            </P>
