            <center><h4>Pudre fungoso de tallo y raíz - Phytophthora parasitica</h4></center>
           <center><img src="images/enfermedad2.jpg" alt=""></center> 
            <br>
            <P>  
            En el caso de la pudrición del corazón de la piña es causada por el Phytophthora parasítica, la cual se ve favorecida por suelos con malos drenajes, encharcamientos y pH altos. Produce un daño en el corazón o meristemo apical de la planta, produce una decoloración de las hojas hasta tornarse rojizas, las hojas se arrancan fácilmente y provoca la muerte de la planta. El daño se presenta poco después de la siembra y puede causar pérdidas del 80 al 90%. El control de esta enfermedad debe de ir orientado a la prevención, algunas recomendaciones son las siguientes:

            a.	Manejo adecuado de los residuos de la cosecha anterior.
            b.	Buena preparación de suelos.
            c.	Evitar encharcamientos o problemas de drenaje
            d.	Manejo adecuado de la semilla en cuanto a calidad y manejo antes y después de la siembra.
            e.	Utilizar productos desinfectantes preventivos y curativos para evitar la presencia de la  enfermedad y curar pequeños brotes.
            f.	Control de plagas para evitar heridas que permitan la entrada de las enfermedades.
            g.	Evitar heridas por prácticas culturales como: control de malezas, aplicación de abonos y enmiendas.

